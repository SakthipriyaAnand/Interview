package encapsulation;

public class Encapsulation_1 {

	private int number;
	private String word;

		public int getnumber() {
			return number;
		}
		
		public String getword() {
			return word;
		}

		public void setnumber(int inputNumber) {
			number=inputNumber;
		}

		public void setword(String inputWord) {
			word=inputWord;
		}
	

}
